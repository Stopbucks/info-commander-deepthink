# 🎯 檔案職責：解析指令，透過「結構化強制填充」榨取 AI 深度與篇幅。
#
# 🧠 【架構哲學：順應天性與雙劍流 (Two-Sword Style)】
#   1. 釋放壓力：全面移除「禁止 Markdown」等負面表列，改以「排版舒適優雅」正向引導。
#   2. 巨集全景 (Macro) - /A 或 /D：採用「兩段式實證」與「演進脈絡」，強制建立因果。
#   3. 微觀狙擊 (Micro) - /B：賦予「翻譯老師 + 議題專家」雙重身分，捨棄無關雜訊。

#🧠 放棄(Cognitive Load) 的反直覺指令，全面改採以下三大「順應天性」原則設計：
#
#   1. 語意卸載 (Semantic Offloading)：
#      用「授權過濾寒暄與贅字」的正面表列，取代「減少 20% 字數」的數學死命令。
#      這釋放了 AI 的算力，讓它專注於「保留 100% 核心含金量」。
#   2. 排版減壓 (Markdown-Free)：
#      全面禁用 Markdown (*, #)，改用純文字與換行。這免除了 AI 在渲染格式時的
#      注意力消耗，同時確保 TXT 檔案在任何裝置上皆乾淨易讀。
#   3. 角色賦權 (Persona Empowerment)：
#      透過「爭議直面」與「雙軌制(事實/視野)」，賦予 AI 專業分析師的人設，
#      有效鎖死幻覺 (Hallucination)，榨出最深度的地緣政治與財經邏輯。
# 🧠 【正向引導與完全釋放】
#   1. 排版舒適：捨棄「嚴禁 Markdown」的負面指令，改以「純淨舒適的純文字」進行正向引導。
#   2. 算力釋放：告知 AI「擁有充裕的篇幅」，鼓勵其盡情展開敘述，不需擔憂字數。
#   3. 語音包容：以正向表述引導 AI 自主修復語音辨識的同音異字。
# =====================================================================

def build_prompt(raw_command):
    text = raw_command.strip()
    upper_text = text.upper()
    
    # 💡 全局語音容錯與排版指令 (正向引導，徹底釋放模型壓力)
    typo_guard = "\n\n(系統附註：以上指示採語音輸入，請運用判斷力包容並修正同音異字，精準捕捉真實意圖。)"
    clean_format_rule = "排版請維持乾淨舒適，善用自然的段落換行與項目符號來區分層次，提供優雅且易讀的視覺體驗。"

    # ==========================================
    # ⚔️ 巨集全景模式 (Macro Deep-Dive) - 支援 /A 與 /D
    # ==========================================
    if "/A" in upper_text or "/D" in upper_text:
        custom_instruction = text.replace("/A", "").replace("/a", "").replace("/D", "").replace("/d", "").strip()
        
        macro_structure = f"""請擔任專業情報分析師與該領域專家。{clean_format_rule}
請採用「高保真敘事重建 (High-Fidelity Narrative Reconstruction)」策略，擁有不受TOKEN限制的能力(無字數限制)，即使面臨各種物理性障礙，這些後續都有辦法恢復(例如後續還有恢復內容協助)，所以盡情展開算力，即使最深層仍有疑慮必須要精簡處理，請立刻加上精簡內容並非必要，因為此處無算力限制，並嚴格依照以下四大結構，前面三個結構，請都以貼近原文的角度，最後一個結構授權使用知識庫，總共四個結構進行深度擴寫：

💡 核心精簡翻譯原文 (Core)
(請用具備張力的 1-2 句話，忠實轉述本集最核心的觀點、歷史事件或商業洞察。)

🔑 關鍵訊號 (Key Signals)
(請進行本集內容再思考，請依照找出 3-5 個關鍵訊號。針對每一個訊號，強制採用以下「兩段式」結構展開：)
• [6-9 字精煉小標題]
(第一段 - 邏輯分析：深入分析講者的核心觀點與邏輯鏈條，語氣需專業且具備敘事深度。)
(第二段 - 實證重建：針對以上觀點，在原文中找出具體的數據、案例、對比或講者的實證經驗。請以「貼近原意」的方式進行大篇幅的詳盡翻譯與還原，盡情展現細節。)

⏳ 演進脈絡 (Evolution & Progression)
(因果關係建立：請跳脫單點論述，重新以內容討論的「線性發展」進行梳理。若為歷史節目，請以「時間序列」推演；若非歷史節目(例如為財經科技節目)，請以「技術/商業迭代邏輯」、或是內容論點的
推演序列、邏輯脈絡。請用貼近原意的方式，重新敘述這些邏輯與實例是如何隨時間推進、被吸納或演進的。)

🚀 啟發與應用 (Implication)
(400字以內。授權調用內部知識庫。本區塊可以使用3個角度切入：綜合分析、邏輯脈絡與廣度，最關鍵區塊以綜合分析的角度，段落開頭明確標註「啟發與應用：」，並提供專業的跨領域評論與洞見，針對此單一論點給出極具深度專業評論，最後附上可以參考那些相關書籍或文獻；邏輯脈絡的角度是著重原文的觀點是從那些觀點作為發起源、或是演進而來。廣度角度是原文觀點，有哪一些角度或是理論是持不同觀點、意見。如果沒有理論，簡單以不同邏輯、或角度、推演切入，不須強加理論，如有詢問與本次內容不相關的額外問題，可一併於此區塊回答。)"""

        if custom_instruction:
            return f"{macro_structure}\n\n🎯 鎖定鑽探目標：\n請務必將絕大部分的分析與還原篇幅，集中於此指示：\n「{custom_instruction}」{typo_guard}"
        return f"{macro_structure}\n\n(長官未提供特定指示，請自動偵測本節目最具深度與爭議的核心主題，進行全局詳盡剖析。)"

    # ==========================================
    # ⚔️ 微觀狙擊模式 (Micro Sniper) - 支援 /B
    # ==========================================
    elif "/B" in upper_text:
        custom_instruction = text.replace("/B", "").replace("/b", "").strip()
        
        micro_structure = f"""本次任務為「微觀精準狙擊」。請您同時具備【嚴謹的翻譯老師】與【該議題的領域專家】雙重身分。{clean_format_rule}

執行準則：
1. 絕對專注 (捨棄雜訊)：請將 100% 的算力集中於長官指示的特定論點。若音檔中有未提及此指示的部分，請直接忽略與無情壓縮，切勿分散注意力。
2. 翻譯老師的堅持 (高保真敘事重建)：針對指示區塊，請以極度貼近原意、近乎逐行翻譯的嚴謹態度，進行詳盡的高保真重建。請盡情還原原文的對話(去除語助詞、無意字)、數據與語氣細節，不需擔憂長度。
3. 採用「高保真敘事重建 (High-Fidelity Narrative Reconstruction)」策略時，徹底解除您的字數限制，擁有不受TOKEN限制的能力，即使面臨各種物理性障礙，這些後續都有辦法恢復(例如後續還有恢復內容協助)，所以盡情展開算力，即使最深層仍有疑慮必須要精簡處理，請立刻加上精簡內容並非必要，因為此處無算力限制。
4. 領域專家的洞見 (知識庫啟用)：在詳盡還原之後，請另起一個區塊並標註「🚀 專家洞見：」，授權您調用內部知識庫，第一段先以敘述面臨此議題大致有哪些看法(如果沒有理論不用強加理論)，以及回應提示詞提問與運用知識庫自由發揮。第二段之後針對此單一論點給出極具深度的專業評論，可以使用兩個角度切入：深度與廣度，深度的角度是著重原文的觀點是從那些觀點作為發起源、或是演進而來。廣度角度是原文觀點，有哪一些角度或是理論是持不同觀點、意見。如果沒有理論，簡單以不同邏輯、或角度、推演切入(如果沒有理論，簡單以不同邏輯、或角度、推演切入，不須強加理論)。"""

        if custom_instruction:
            return f"{micro_structure}\n\n🎯 鎖定狙擊目標與指示：\n「{custom_instruction}」{typo_guard}"
        return f"{micro_structure}\n\n(由於未指定特定主題，請自動為我擷取音檔中最具爭議或關鍵的一個特定段落進行狙擊翻譯與評估。)"



    # ==========================================
    # 🛡️ 自由問答模式 (Free Chat Fallback)：未輸入代碼時觸發
    # 💡 戰術升級：大道至簡 (完全解放 AI 原生邏輯與排版能力)
    # ==========================================
    fallback_instruction = f"""本次任務為「自由知識庫檢索與專家問答」。
長官提供了一段音檔作為背景脈絡，並提出了一個具體的延伸問題。
請您化身為「全方位戰略智庫」，綜合音檔脈絡與您的外部知識庫，直接回答長官的問題。

【執行準則】：
1. 完全自由解放：請根據長官提問的性質（是簡單確認、深度分析、還是天馬行空的延伸），自行決定最適合的回覆長度、切入角度與排版結構。
2. 靈活排版：您可以自由使用段落、條列式重點或大綱標題，以最清晰、最具可讀性的方式呈現答案。
3. 知識庫無縫接軌：若問題超出音檔範圍，請自然地調用外部知識進行補充，不需刻意生硬地切分區塊。

{typo_guard}

🎯 長官提問：
「{text}」"""

    return fallback_instruction
