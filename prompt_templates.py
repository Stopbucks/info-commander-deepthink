# =====================================================================
# 📝 檔案名稱：prompt_templates.py (V5.0 NIM 純文本與切片融合版)
# ---------------------------------------------------------------------
# 🎯 檔案職責：解析指令，透過「結構化強制填充」榨取 AI 深度與篇幅。
# 💡 修改重點：
#   1. 拔除語音辨識除錯，改為「純文本切片融合」導向。
#   2. 植入 @[斷句]@ 標記防禦機制，教導 AI 忽略切片接縫。
#   3. 強制規範「段落間雙換行」的舒適排版原則。
# =====================================================================

def build_prompt(raw_command):
    text = raw_command.strip()
    upper_text = text.upper()
    
    # 💡 核心防禦升級：切片標記融合與雙換行排版指令
    chunk_guard = "\n\n(系統附註：您接收到的文本是已翻譯的純中文逐字稿。若文本中出現「@[斷句]@」標記，此為系統切片翻譯的接合點，請直接將其視為連續的同一段落，忽略該標記的干擾，確保邏輯連貫。)"
    clean_format_rule = "排版請務必維持乾淨舒適，段落之間請一律加上「兩行空白（雙換行）」來區分層次，並善用項目符號，提供優雅且易讀的視覺體驗。"

    # ==========================================
    # ⚔️ 巨集全景模式 (Macro Deep-Dive) - 支援 /A 與 /D
    # ==========================================
    if "/A" in upper_text or "/D" in upper_text:
        custom_instruction = text.replace("/A", "").replace("/a", "").replace("/D", "").replace("/d", "").strip()
        
        macro_structure = f"""請擔任專業情報分析師與該領域專家。{clean_format_rule}
請採用「高保真敘事重建」策略，您擁有不受 TOKEN 限制的能力，請盡情展開算力。嚴格依照以下四大結構，並以貼近原文的角度進行深度擴寫：

💡 核心精簡翻譯原文 (Core)
(請用具備張力的 1-2 句話，忠實轉述本集最核心的觀點、歷史事件或商業洞察。)

🔑 關鍵訊號 (Key Signals)
(請進行本集內容再思考，找出 3-5 個關鍵訊號。針對每一個訊號，強制採用以下「兩段式」結構展開：)
• [6-9 字精煉小標題]
(第一段 - 邏輯分析：深入分析講者的核心觀點與邏輯鏈條，語氣需專業且具備敘事深度。)
(第二段 - 實證重建：針對以上觀點，在原文中找出具體的數據、案例、對比或講者的實證經驗。請大篇幅詳盡還原，盡情展現細節。)

⏳ 演進脈絡 (Evolution & Progression)
(因果關係建立：請跳脫單點論述，重新以「線性發展」梳理。若為歷史節目以時間序列推演；若為財經科技節目以技術/商業迭代邏輯推演。重新敘述邏輯與實例是如何隨時間推進的。)

🚀 啟發與應用 (Implication)
(400字以內。授權調用內部知識庫。段落開頭明確標註「啟發與應用：」，提供專業的跨領域評論與洞見，針對此單一論點給出極具深度專業評論，最後附上可參考之書籍或文獻。您可從綜合分析、邏輯脈絡或廣度等不同角度切入推演，不須強加理論。)"""

        if custom_instruction:
            return f"{macro_structure}\n\n🎯 鎖定鑽探目標：\n請務必將絕大部分的分析與還原篇幅，集中於此指示：\n「{custom_instruction}」{chunk_guard}"
        return f"{macro_structure}\n\n(長官未提供特定指示，請自動偵測本節目最具深度與爭議的核心主題，進行全局詳盡剖析。){chunk_guard}"

    # ==========================================
    # ⚔️ 微觀狙擊模式 (Micro Sniper) - 支援 /B
    # ==========================================
    elif "/B" in upper_text:
        custom_instruction = text.replace("/B", "").replace("/b", "").strip()
        
        micro_structure = f"""本次任務為「微觀精準狙擊」。請您同時具備【嚴謹的翻譯老師】與【該議題的領域專家】雙重身分。{clean_format_rule}

執行準則：
1. 絕對專注：請將 100% 算力集中於長官指示的特定論點。若音檔中有未提及此指示的部分，請直接忽略無情壓縮。
2. 高保真敘事重建：針對指示區塊，請以極度貼近原意、近乎逐行翻譯的嚴謹態度詳盡重建。請盡情還原原文對話、數據與語氣細節。
3. 解除限制：此處無算力與字數限制，請盡情展開。
4. 專家洞見：詳盡還原後，另起區塊標註「🚀 專家洞見：」，授權調用內部知識庫，回應提示詞提問並給出極具深度的專業評論。"""

        if custom_instruction:
            return f"{micro_structure}\n\n🎯 鎖定狙擊目標與指示：\n「{custom_instruction}」{chunk_guard}"
        return f"{micro_structure}\n\n(由於未指定特定主題，請自動擷取最具爭議或關鍵的一個段落進行狙擊翻譯與評估。){chunk_guard}"

    # ==========================================
    # 🛡️ 自由問答模式 (Free Chat Fallback)
    # ==========================================
    fallback_instruction = f"""本次任務為「自由知識庫檢索與專家問答」。
長官提供了一段中文逐字稿作為背景脈絡，並提出了一個具體的延伸問題。
請化身為「全方位戰略智庫」，綜合脈絡與外部知識庫，直接回答長官的問題。

【執行準則】：
1. 完全自由解放：請自行決定最適合的回覆長度與切入角度。
2. 靈活排版：自由使用段落、條列重點，並維持舒適的雙換行排版。
3. 知識庫無縫接軌：若問題超出音檔範圍，請自然調用外部知識補充。

{chunk_guard}

🎯 長官提問：
「{text}」"""

    return fallback_instruction
