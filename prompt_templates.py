import re

def build_prompt(raw_command):
    text = raw_command.strip()
    upper_text = text.upper()
    
    chunk_guard = "\n\n(系統附註：您接收到的文本是已翻譯的純中文逐字稿。若文本中出現「@[斷句]@」標記，此為系統切片翻譯的接合點，請直接將其視為連續的同一段落，忽略該標記的干擾，確保邏輯連貫。)"
    clean_format_rule = "排版請務必維持乾淨舒適，段落之間請一律加上「兩行空白（雙換行）」來區分層次，並善用項目符號，提供優雅且易讀的視覺體驗。"

    # ==========================================
    # ⚔️ 巨集全景模式 (支援 /A, /AG, /D, /DG)
    # ==========================================
    if re.search(r'/[AD]G?', upper_text):
        # 剝除指令標籤 (例如 /ag, /A 等)
        custom_instruction = re.sub(r'/[AaDd][Gg]?', '', text).strip()
        
        macro_structure = f"""請擔任專業情報分析師與該領域專家。{clean_format_rule}
請採用「高保真敘事重建」策略，您擁有不受 TOKEN 限制的能力，請盡情展開算力。嚴格依照以下四大結構，並以貼近原文的角度進行深度擴寫：

💡 核心精簡翻譯原文 (Core)
(請用具備張力的 1-2 句話，忠實轉述本集最核心的觀點、歷史事件或商業洞察。)

🔑 關鍵訊號 (Key Signals)
(請進行本集內容再思考，找出 3-5 個關鍵訊號。針對每一個訊號，強制採用以下「兩段式」結構展開：)
• [6-9 字精煉小標題]
(第一段 - 邏輯分析：深入分析講者的核心觀點與邏輯鏈條，語氣需專業且具備敘事深度。)
(第二段 - 實證重建：針對以上觀點，在原文中找出具體的數據、案例、對比或講者的實證經驗。請大篇幅詳盡還原，盡情展現細節。)

⏳ 演進脈絡 (Evolution & Progression)
(因果關係建立：請跳脫單點論述，重新以「線性發展」梳理。若為歷史節目以時間序列推演；若為財經科技節目以技術/商業迭代邏輯推演。)

🚀 啟發與應用 (Implication)
(400字以內。授權調用內部知識庫。段落開頭明確標註「啟發與應用：」，提供專業跨領域評論，並附上可參考之書籍或文獻。)"""

        if custom_instruction:
            return f"{macro_structure}\n\n🎯 鎖定鑽探目標：\n請務必將分析篇幅集中於此指示：\n「{custom_instruction}」{chunk_guard}"
        return f"{macro_structure}\n\n(長官未提供特定指示，請自動偵測本節目最具爭議的核心主題進行剖析。){chunk_guard}"

    # ==========================================
    # ⚔️ 微觀狙擊模式 (支援 /B, /BG)
    # ==========================================
    elif re.search(r'/BG?', upper_text):
        custom_instruction = re.sub(r'/[Bb][Gg]?', '', text).strip()
        
        micro_structure = f"""本次任務為「微觀精準狙擊」。請您同時具備【嚴謹的翻譯老師】與【該議題的領域專家】雙重身分。{clean_format_rule}

執行準則：
1. 絕對專注：請將 100% 算力集中於指示的特定論點，直接忽略未提及的部分。
2. 高保真敘事重建：針對指示區塊，請以極度貼近原意詳盡重建，還原數據與語氣細節。
3. 解除限制：此處無算力與字數限制。
4. 專家洞見：詳盡還原後，另起區塊標註「🚀 專家洞見：」，授權調用內部知識庫給出極具深度的專業評論。"""

        if custom_instruction:
            return f"{micro_structure}\n\n🎯 鎖定狙擊目標與指示：\n「{custom_instruction}」{chunk_guard}"
        return f"{micro_structure}\n\n(未指定特定主題，請自動擷取關鍵段落進行狙擊翻譯。){chunk_guard}"

    # ==========================================
    # 🛡️ 自由問答模式 
    # ==========================================
    return f"""本次任務為「自由知識庫檢索與專家問答」。綜合脈絡與外部知識庫，直接回答長官的問題。{chunk_guard}\n\n🎯 長官提問：\n「{text}」"""
