
# ---------------------------------------------------------
# 程式碼：prompt_templates.py (獨立提示詞庫與解析引擎)
# 職責：解析使用者的動態指令，組合出給 Gemini 的最終長篇提示詞。
# ---------------------------------------------------------
# 🎯 檔案職責：
#   作為獨立的「字串處理模組」，負責解析指揮官在 Telegram 下達的動態指令。
#   自動分離「戰術代碼」與「自訂指示」，並組合出具備「防幻覺、爭議直面、
#   雙軌制 (事實85%/視野15%)」的高階提示詞，交由主程式餵給 AI。
#
# ⚙️ 支援的戰術代碼 (大小寫不拘，如 /A 或 /a 皆可觸發)：
#   ➤ /A : 【全文深度翻譯】
#          提供完整繁體中文翻譯，並依據前方/後方提及的「自訂指示」進行邏輯總結。
#   ➤ /B : 【精準狙擊翻譯】
#          僅擷取與「自訂指示」高度相關的段落進行全文翻譯與專業評估；
#          相關度低的段落則自動進行「降噪統整」。
#   ➤ /C : 【純淨英聽還原】
#          產出 100% 完整純英文逐字稿 (Transcript)，不加任何摘要或中文翻譯，
#          精準保留原始語意與商業推廣資訊。
#
# 💡 實戰範例：
#   在 TG 輸入：「古巴經濟現況 /b」
#   系統將自動套用 /B 版型，並將「古巴經濟現況」作為核心指示鎖定分析。
# =====================================================================
def build_prompt(raw_command):
    """
    智慧解析器：自動分離 /A, /B, /C 代碼與使用者的自訂指示，並套用對應版型。
    """
    text = raw_command.strip()
    upper_text = text.upper()

    # ==========================================
    # 💥 /A 模式：【全文翻譯 + 前面提及指示】
    # ==========================================
    if "/A" in upper_text:
        custom_instruction = text.replace("/A", "").replace("/a", "").strip()
        
        # 💡 重大修正：使用 """ 包裝多行字串，避免 SyntaxError 崩潰
        base_prompt = """請擔任專業情報分析師。請提供這段音檔的【完整繁體中文全文翻譯】。並嚴格遵守以下原則：

1. 爭議直面：遇到具備爭議性、對立性或敏感議題時（戰爭、政治、市場崩潰等），嚴禁迴避或粉飾。請以中立視角呈現講者的邏輯鏈條，並將其視為「真實世界的風險變數」。
2. 商業識別：若涉及贊助產品、置入性廣告，英聽翻譯時請在該段落開頭加註：「📢 商業促銷預警：以下包含產品推廣資訊。」，摘要或是評論時，請直接忽略。
3. 嚴守分析比例：進行摘要或統整評估時，必須遵守雙軌制：
   - 第一軌：事實層 (85%)：深入分析講者邏輯與風險變數，內容必須 100% 忠於音檔。
   - 第二軌：視野層 (15%)：僅限於【📚 視野拓展】區塊，授權你調用知識庫補充背景。"""

        if custom_instruction:
            return f"{base_prompt}\n\n此外，請務必針對以下核心指示進行深度執行與總結：\n「{custom_instruction}」"
        return f"{base_prompt}\n\n請在翻譯完成後，為我總結這篇情報的核心邏輯。"

    # ==========================================
    # 💥 /B 模式：【部分相關翻譯 + 前面提及指示】
    # ==========================================
    elif "/B" in upper_text:
        custom_instruction = text.replace("/B", "").replace("/b", "").strip()
        
        base_prompt = """請擔任專業情報分析師。請【僅針對與下列主題高度相關】的英文段落進行重點擷取與繁體中文翻譯。並嚴格遵守以下原則：

1. 爭議直面：遇到爭議性或敏感議題時，嚴禁粉飾，請中立呈現邏輯鏈條，視為風險變數。
2. 商業識別：涉及贊助產品時，請於翻譯段落加註「📢 商業促銷預警」，於摘要評論時直接忽略。
3. 降噪統整：翻譯相關英文段落時，請把不相關的段落以簡單、貼近原意的敘述方式統整成 2 個段落以內，段落最後標註「(統整原意)」。接續相關概念的段落請直接全文翻譯，並於開頭註明「(本段相關，翻譯如下)」。
4. 關聯度判定：若整段落反覆陳述該概念達 60% 以上，認定高度相關；若僅 30% 提及該概念但討論其他可能影響，則以統整方式處理，並標註「(統整原意)」。
5. 嚴守分析比例：評論與評估需遵守雙軌制：第一軌 事實層 (85%) 忠於音檔；第二軌 視野層 (15%) 僅限於【📚 視野拓展】區塊補充背景。"""

        if custom_instruction:
            return f"{base_prompt}\n\n🎯 鎖定主題與執行指示：\n「{custom_instruction}」\n\n請在翻譯相關段落後，依據上述指示給出您的專業評估與短評。"
        return f"{base_prompt}\n\n(由於未指定特定主題，請自動為我擷取音檔中最具爭議或關鍵的數據段落進行翻譯與評估。)"

    # ==========================================
    # 💥 /C 模式：【純粹英聽全文逐字稿】
    # ==========================================
    elif "/C" in upper_text:
        custom_instruction = text.replace("/C", "").replace("/c", "").strip()
        
        base_prompt = """請擔任專業聽譯人員。我需要這段音檔的【完整純英文逐字稿】 (English Transcript)。
請務必精準還原語意，不要做任何摘要、刪減或中文翻譯。若有商業廣告，也請如實聽寫。"""

        if custom_instruction:
            return f"{base_prompt}\n\n(請特別留意或標註與此主題相關的段落：「{custom_instruction}」)"
        return base_prompt

    # ==========================================
    # 🛡️ Fallback 模式：若未輸入任何代碼，原句奉還
    # ==========================================
    return text
