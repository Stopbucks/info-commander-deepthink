# 🎯 檔案職責：
#   解析指揮官在 Telegram 下達的動態指令，並自動組合出給 AI 的長篇提示詞。
#
# 🧠 【架構哲學：順應 Gemini 天性 (LLM-Native Design)】
#   本引擎徹底拋棄了「字數計算」、「強制數學比例」等會造成 LLM 嚴重認知負擔
#   (Cognitive Load) 的反直覺指令，全面改採以下三大「順應天性」原則設計：
#
#   1. 語意卸載 (Semantic Offloading)：
#      用「授權過濾寒暄與贅字」的正面表列，取代「減少 20% 字數」的數學死命令。
#      這釋放了 AI 的算力，讓它專注於「保留 100% 核心含金量」。
#   2. 排版減壓 (Markdown-Free)：
#      全面禁用 Markdown (*, #)，改用純文字與換行。這免除了 AI 在渲染格式時的
#      注意力消耗，同時確保 TXT 檔案在任何裝置上皆乾淨易讀。
#   3. 角色賦權 (Persona Empowerment)：
#      透過「爭議直面」與「雙軌制(事實/視野)」，賦予 AI 專業分析師的人設，
#      有效鎖死幻覺 (Hallucination)，榨出最深度的地緣政治與財經邏輯。
#
# ⚙️ 支援的戰術代碼 (大小寫不拘)：
#   ➤ /A : 【全文翻譯 + 深度分析】
#   ➤ /B : 【精準狙擊翻譯 + 降噪統整】
#   ➤ /C : 【高保真精粹翻譯】(授權 AI 主動降噪，產出極度流暢的純英文/原文還原)
# ⚙️ 支援戰術代碼：
#   ➤ /D : 【巨獸檔案降級專用】(系統自動觸發，分配 80% 算力於核心指示，20% 極限降噪)
# =====================================================================

def build_prompt(raw_command):
    """
    智慧解析器：自動分離 /A, /B, /C 代碼與使用者的自訂指示，並套用對應版型。
    """
    text = raw_command.strip()
    upper_text = text.upper()

    # ==========================================
    # 💥 /A 模式：【全文翻譯 + 前面提及指示】
    # ==========================================
    if "/A" in upper_text:
        custom_instruction = text.replace("/A", "").replace("/a", "").strip()
        
        base_prompt = """請擔任專業情報分析師與首席翻譯官。請使用「純文字格式（僅透過換行區隔，絕對禁止使用星號或井字號）」執行以下兩項獨立任務：

第一部分：全文翻譯
- 核心要求：完整翻譯音檔內容。若音檔極長，授權您對「次要資訊（如講者寒暄、冗長過場、無意義的語氣詞）」進行高密度的語意濃縮；但「核心數據、邏輯鏈條與風險變數」必須 100% 完整保留。
- 商業識別：遇贊助廣告請加註「📢 商業促銷預警」，評論時直接忽略。

第二部分：情報深度分析
- 爭議直面：遇到具備爭議性、對立性或敏感議題時（如戰爭、政治、市場崩潰），嚴禁迴避或粉飾。請中立呈現講者的邏輯鏈條，視為「風險變數」。
- 雙軌制精簡分析：請以精簡扼要的篇幅，提供事實層（深入分析講者邏輯，忠於音檔）與視野層（調用知識庫補充宏觀背景）的總結。"""

        if custom_instruction:
            return f"{base_prompt}\n\n此外，在「第二部分」的分析中，請務必針對以下核心指示進行深度總結：\n「{custom_instruction}」"
        return f"{base_prompt}\n\n請在「第二部分」為我總結這篇情報的核心邏輯。"

    # ==========================================
    # 💥 /B 模式：【部分相關翻譯 + 前面提及指示】
    # ==========================================
    elif "/B" in upper_text:
        custom_instruction = text.replace("/B", "").replace("/b", "").strip()
        
        base_prompt = """請擔任專業情報分析師。請使用「純文字格式（僅透過換行區隔，絕對禁止使用星號或井字號）」執行任務。

針對下方指定的鎖定主題，請遵守以下原則處理：
1. 相關段落翻譯：針對與主題高度相關的段落，進行完整翻譯，並於段落開頭註明「(本段相關)」。
2. 降噪統整：針對不相關或低相關段落，請高度濃縮成簡短的語意過場，並標註「(統整原意)」。請確保核心重點不遺漏。
3. 爭議直面：中立呈現敏感議題，嚴禁粉飾。
4. 深度分析：在翻譯段落結束後，請另起區塊，以精簡篇幅進行雙軌制（事實層/視野層）的專業短評。"""

        if custom_instruction:
            return f"{base_prompt}\n\n🎯 鎖定主題與執行指示：\n「{custom_instruction}」"
        return f"{base_prompt}\n\n(由於未指定特定主題，請自動為我擷取音檔中最具爭議或關鍵的數據段落進行翻譯與評估。)"

    # ==========================================
    # 💥 /C 模式：【高保真精粹翻譯】(減壓卸載版)
    # ==========================================
    elif "/C" in upper_text:
        custom_instruction = text.replace("/C", "").replace("/c", "").strip()
        
        base_prompt = """請擔任專業情報編譯官。我需要這段音檔的【高保真精粹英文還原】 (High-Fidelity Distilled Transcript)。請使用純文字格式（僅透過換行區隔，絕對禁止使用星號或井字號），並遵守以下正向編譯原則：

1. 授權降噪 (卸載負擔)：請發揮專業編輯能力，主動過濾掉講者的口語贅字、重複語句、自我糾正與無意義的過場寒暄。
2. 資訊保真：過濾雜訊後，請將剩餘的高含金量內容轉錄為極度流暢的「純英文」。務必 100% 保留所有的數據、核心論點、專有名詞與風險變數。絕不進行中文翻譯。
3. 商業識別：若聽見贊助廣告，請加註「📢 商業促銷預警」並簡述品牌即可。
4. 結構重組：請將講者發散的思緒，重新組織成邏輯連貫、易於閱讀的英文段落。"""

        if custom_instruction:
            return f"{base_prompt}\n\n此外，在編譯過程中，請特別放大並詳細轉錄與此指示相關的段落：\n「{custom_instruction}」"
        return base_prompt

# ==========================================
    # 🛡️ /D 模式：【巨獸防禦專用】(長官指定的大質量檔案專用模板)
    # ==========================================
    elif "/D" in upper_text:
        custom_instruction = text.replace("/D", "").replace("/d", "").strip()
        
        base_prompt = """【⚠️ 系統最高優先級指令：大質量檔案逆向鑽探】
請擔任最高階的情報編譯官。系統偵測到此音檔為超長篇巨獸檔案（推測超過 2~4 小時）。
為避免系統過載與輸出截斷，請強制進入「精準鑽探模式」，並嚴格遵守以下純文字格式排版（絕對禁止使用星號或井字號）：

1. 脈絡認知與領域自適應：
此檔案已由第一線部隊完成基礎輪廓摘要。您現在的任務是「第二波深度逆向工程」，無需進行全篇流水帳翻譯。請根據提示詞指示的語境（歷史沿革、商業邏輯、科技硬體等），自動切換您的分析視角。

2. 算力集中 (80% 篇幅)：
請將絕大多數的篇幅與輸出算力，完全集中於回覆提示詞下達的【核心指示】。在容許的範圍內，盡可能逼近提示詞所指涉、想理解的真相，以此主題的原文資訊進行大量、深度的精粹翻譯與還原。

3. 極限降噪 (20% 篇幅)：
對於與提示詞指示無關的段落，請採取最無情的壓縮，僅用幾句話帶過語意轉折即可，甚至可以直接略過，本次可採極限壓縮。

4. 雙軌分析：
在深度還原完畢後，請於文末提供精簡的視野層短評(容許使用知識庫，開頭標註短評，即可知道動用知識庫進行短評)。"""

        if custom_instruction:
            return f"{base_prompt}\n\n🎯 鎖定鑽探目標與核心指示：\n「{custom_instruction}」\n\n請直接開始針對此目標進行深度鑽探。"
        return f"{base_prompt}\n\n(長官未提供特定指示，請自動偵測本節目最具深度與爭議的核心主題，進行極限精華剖析。)"


    # ==========================================
    # 🛡️ Fallback 模式：若未輸入任何代碼，原句奉還
    # ==========================================
    return text
