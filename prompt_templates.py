# 🎯 檔案職責：
#   解析指揮官在 Telegram 下達的動態指令，並自動組合出給 AI 的長篇提示詞。
#
# 🧠 【架構哲學：順應 Gemini 天性 (LLM-Native Design)】
#   本引擎徹底拋棄了「字數計算」、「強制數學比例」等會造成 LLM 嚴重認知負擔
#   (Cognitive Load) 的反直覺指令，全面改採以下三大「順應天性」原則設計：
#
#   1. 語意卸載 (Semantic Offloading)：
#      用「授權過濾寒暄與贅字」的正面表列，取代「減少 20% 字數」的數學死命令。
#      這釋放了 AI 的算力，讓它專注於「保留 100% 核心含金量」。
#   2. 排版減壓 (Markdown-Free)：
#      全面禁用 Markdown (*, #)，改用純文字與換行。這免除了 AI 在渲染格式時的
#      注意力消耗，同時確保 TXT 檔案在任何裝置上皆乾淨易讀。
#   3. 角色賦權 (Persona Empowerment)：
#      透過「爭議直面」與「雙軌制(事實/視野)」，賦予 AI 專業分析師的人設，
#      有效鎖死幻覺 (Hallucination)，榨出最深度的地緣政治與財經邏輯。
#
# ⚙️ 支援的戰術代碼 (大小寫不拘)：
#   ➤ /A : 【全文翻譯 + 深度分析】
#   ➤ /B : 【精準狙擊翻譯 + 降噪統整】
#   ➤ /C : 【高保真精粹翻譯】(授權 AI 主動降噪，產出極度流暢的純英文/原文還原)
# ⚙️ 支援戰術代碼：
#   ➤ /D : 【巨獸檔案降級專用】(系統自動觸發，分配 80% 算力於核心指示，20% 極限降噪)
# =====================================================================

# 🧠 【架構哲學：正向引導與完全釋放】
#   1. 排版舒適：捨棄「嚴禁 Markdown」的負面指令，改以「純淨舒適的純文字」進行正向引導。
#   2. 算力釋放：告知 AI「擁有充裕的篇幅」，鼓勵其盡情展開敘述，不需擔憂字數。
#   3. 語音包容：以正向表述引導 AI 自主修復語音辨識的同音異字。
# =====================================================================

def build_prompt(raw_command):
    text = raw_command.strip()
    upper_text = text.upper()
    
    # 💡 語音容錯指令 (正向表述)：鼓勵 AI 發揮理解力
    typo_guard = "\n\n(系統附註：以上指示為語音輸入，請運用您的專業判斷，包容並修正可能的同音異字，精準捕捉指示的真實意圖。)"

    # 💡 舒適排版指令 (正向表述)：取代過去的「嚴禁使用星號」
    clean_format_rule = "排版請維持乾淨舒適的純文字風格，善用自然的段落換行與空白來區分層次，提供優雅的閱讀體驗。"

    # ==========================================
    # 💥 /A 模式：【全文翻譯 + 前面提及指示】
    # ==========================================
    if "/A" in upper_text and not "/D" in upper_text:
        custom_instruction = text.replace("/A", "").replace("/a", "").strip()
        
        base_prompt = f"""請擔任專業情報分析師與首席翻譯官。{clean_format_rule}
請執行以下兩項任務：

第一部分：全文詳盡翻譯
請運用您充裕的篇幅，盡可能詳盡地還原音檔內容。保留核心數據、邏輯鏈條與風險變數。遇贊助廣告請加註「📢 商業促銷預警」即可。

第二部分：情報深度分析
請直面爭議性或敏感議題，中立呈現講者的邏輯。並提供事實層（忠於原音）與視野層（補充宏觀背景）的專業短評。"""

        if custom_instruction:
            return f"{base_prompt}\n\n在「第二部分」的分析中，請務必針對以下指示進行深度總結：\n「{custom_instruction}」{typo_guard}"
        return f"{base_prompt}\n\n請在「第二部分」為我總結這篇情報的核心邏輯。"

    # ==========================================
    # 💥 /B 模式：【部分相關翻譯 + 前面提及指示】
    # ==========================================
    elif "/B" in upper_text and not "/D" in upper_text:
        custom_instruction = text.replace("/B", "").replace("/b", "").strip()
        
        base_prompt = f"""請擔任專業情報分析師。{clean_format_rule}

針對下方指定的鎖定主題，請盡情發揮您的解析能力：
1. 詳盡還原：針對高度相關的段落，請給出極度詳盡的長篇幅翻譯（開頭請註明「本段相關」）。您可以自由展開，無需刻意精簡。
2. 語意過場：針對不相關的段落，請濃縮成簡短的語意過場（標註「統整原意」），以保持脈絡連貫。
3. 爭議直面與分析：在翻譯結束後，請另起區塊，進行事實與視野層的專業點評。"""

        if custom_instruction:
            return f"{base_prompt}\n\n🎯 鎖定主題與執行指示：\n「{custom_instruction}」{typo_guard}"
        return f"{base_prompt}\n\n(未指定主題，請自動擷取最具爭議或關鍵的數據段落進行詳盡翻譯與評估。)"

    # ==========================================
    # 💥 /C 模式：【高保真精粹翻譯】
    # ==========================================
    elif "/C" in upper_text and not "/D" in upper_text:
        custom_instruction = text.replace("/C", "").replace("/c", "").strip()
        
        base_prompt = f"""請擔任專業情報編譯官。我需要這段音檔的【高保真精粹英文還原】。{clean_format_rule}

請發揮您的專業編輯能力：主動過濾口語贅字與無意義的寒暄。過濾雜訊後，請將剩餘的高含金量內容，轉錄為極度流暢、結構連貫的純英文段落。請盡情還原細節，務必保留所有的數據與核心論點。"""

        if custom_instruction:
            return f"{base_prompt}\n\n請特別放大並詳細轉錄與此指示相關的段落：\n「{custom_instruction}」{typo_guard}"
        return base_prompt

    # ==========================================
    # 🛡️ /D 模式：【巨獸防禦專用】(大質量檔案專用模板)
    # ==========================================
    elif "/D" in upper_text:
        custom_instruction = text.replace("/D", "").replace("/d", "").strip()
        
        base_prompt = f"""【系統最高優先級指令：大質量檔案逆向鑽探】
請擔任最高階的情報編譯官。系統偵測此音檔為超長篇巨獸檔案，您現在的任務是「第二波深度逆向工程」。{clean_format_rule}

請徹底解除您的字數限制，盡情燃燒算力：
1. 領域自適應：請根據指示的語境（歷史、商業、科技等），自動切換最適合的分析視角。
2. 極致展開 (核心任務)：請將絕大多數的篇幅，完全集中於回覆長官的指示。請給出「極度詳盡、段落式的長篇還原」。請盡可能重現講者的完整論述過程、關鍵對話與細節。您可以自由且詳盡地書寫，請盡情使用您的輸出額度。
3. 邊角降噪：對於無關段落，請用最無情的壓縮，一句話帶過或略過即可。
4. 視野拓展：在長篇詳盡還原後，請於文末提供精簡的視野層洞察短評。"""

        if custom_instruction:
            return f"{base_prompt}\n\n🎯 鎖定鑽探目標與核心指示：\n「{custom_instruction}」{typo_guard}\n\n請直接開始針對此目標進行大篇幅的詳盡深度鑽探。"
        return f"{base_prompt}\n\n(長官未提供特定指示，請自動偵測核心主題，進行大篇幅的詳盡剖析。)"

    return text
